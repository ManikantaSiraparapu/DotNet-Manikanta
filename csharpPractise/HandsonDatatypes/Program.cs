namespace HandsonDatatypes
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //VALUE DATATYPES
            //number types
            byte b = 120;
            short s = 1234;
            int i = 12345;
            long l = 12345678;
            Console.WriteLine(l);
            //fraction types
            double d = 1.234;
            float f = 1.23f;
            decimal dc = 1.23456m;
            Console.WriteLine(f);
            //char type
            char c = 'm';
            Console.WriteLine(c);
            //boolean type
            bool res = true;
            Console.WriteLine(res);

            //REFERENCE TYPES
            //String type
            String name = "Mani";
            String country = "India";
            String number="12345";
            Console.WriteLine(name);
        }
    }
}
